package com.yedam.product;

public class Products {
	private String Name;
	private int Price;
	
	void setName(String name) {
		this.Name = name;
	}
	String getName() {
		return this.Name;
	}
	void setPrice(int price) {
		this.Price = price;
	}
	int getPrice() {
		return this.Price;
	}
}
